
public class Sample {

	private int R;
	private int G;
	private int B;
	private int color;
	
	public Sample (int R, int G, int B, int color) {
		this.R = R;
		this.G = G;
		this.B = B;
		this.color = color;
	}
	
	public int getR() {
		return R;
	}
	
	public int getG() {
		return G;
	}
	
	public int getB() {
		return B;
	}
	
	public int getColor() {
		return color;
	}
}
